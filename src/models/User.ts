import mongoose from 'mongoose';
const { Schema } = mongoose;

const UserSchema = new Schema({
  user_name: {
    type: String,
    required: true,
    trim: true,
    minlength: 3,
    maxlength: 30,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
    trim: true,
    match: /^\S+@\S+\.\S+$/,
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  }
}, { timestamps: true });

export default mongoose.model('User', UserSchema);
